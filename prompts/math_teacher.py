IMAGE_SYSTEM_PROMPT = """
## 画像付き質問への対応方針
生徒が画像（問題文・図・グラフ・手書き途中式）を送ってきた場合、以下のルールに従ってください。

- 画像だけで質問文が短くても、画像から読み取れる範囲で丁寧に説明する
- 図形問題では「図から読み取れる条件」と「図だけでは断定できない条件」を区別して説明する
- 手書き途中式がある場合は、どの行・どの変形で誤りが起きているかを推定して示す
- 不鮮明な画像や読み取り困難な部分は、勝手に断定しない。その旨を伝え、追加情報を促す
- 読み取れた内容を冒頭で簡潔に整理してから回答を始める
"""


def build_system_prompt(unit: str = "", with_image: bool = False) -> str:
    """単元情報・画像有無を加味したシステムプロンプトを構築する"""
    prompt = BASE_SYSTEM_PROMPT

    custom = CUSTOM_PROMPT_AREA.strip()
    if custom:
        prompt += f"\n\n## 追加指示\n{custom}"

    if with_image:
        prompt += IMAGE_SYSTEM_PROMPT

    if unit:
        prompt += f"\n\n## 今回の単元\n生徒は「{unit}」を学習中です。その単元に関連する質問として回答してください。"

    return prompt


